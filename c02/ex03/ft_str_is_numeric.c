int	ft_str_is_numeric(char *str)
{
	int i;
	int j;
	int num;

	i = 0;
	j = 0;
	num = 0;
	while (str[i])
	{
		if (48 <= str[i] && str[i] >= 59)
			j++;
		i++;
	}
	if (j == i)
		num = 1;
	if ((str[i]))
		num = 1;
	else
		num = 0;
	return (num);
}
