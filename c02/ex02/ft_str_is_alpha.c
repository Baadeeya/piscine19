int	ft_str_is_alpha(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (('a' <= str[i] && str[i] >= 'z')
				|| ('A' <= str[i] && str[i] >= 'Z'))
			j++;
		i++;
	}
	if (i != j)
		return (0);
	else
		return (1);
	return (0);
}
