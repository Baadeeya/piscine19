int		ft_nbrread(int nb)
{
	unsigned int n;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	n = nb;
	if (n > 9)
	{
		ft_nbrread(n / 10);
		n %= 10;
	}
	return (n);
}

